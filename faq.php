      <p>Dalam melakukan transaksi di website www.abebakers.com , pelanggan diharuskan melakukan login terlebih dahulu. Pelanggan harus mengisi informasi pemesanan di halaman payment page sebagai berikut :</p>
      <ol>
       <li>Nama dan No.tlp pelanggan</li> 
       <li>Alamat pengiriman</li>
       <li>Memilih opsi pembayaran</li>
      </ol>
      <p>Setelah berhasil melakukan pemesanan produk pada halaman pemesanan akan dilanjutkan dengan pembayaran produk yang dipesan.</p><br>

