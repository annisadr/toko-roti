    <h2 id="howtoorder">HOW TO ORDER</h2><br>
  
      <h4>Bagaimana cara menentukan waktu pengiriman pesanan?</h4>
      <p>Saat pelanggan selesai melakukan konfirmasi pemesanan maka dalam 2 hari pesanan anda akan kami proses dan kami antar</p><br>

