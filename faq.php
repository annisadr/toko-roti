      <p>Pesanan dapat diwakilkan oleh orang lain dengan menandatangani nota pengambilan.</p><br>

      <h4>Jika terjadi masalah pada product Abe's Bakers apakah Customer mendapat jaminan?</h4>
      <p>Abe's Bakers selalu mengutamakan kepuasan Customer, jika terjadi masalah pada produk kami, silahkan kami dalam waktu 1 x 24 jam setelah pesanan diterima.</p><br>

